export const generatedDevices = {};

export function initDemoData() {
	const testDevice = "testDevice" + Math.round(Math.random() * 360) + new Date().toISOString();
	createNewDevice(testDevice);
}

export function createNewDevice(deviceID) {

	const route = route1;
	const {
		latlngs,
		secondsPerPoint
	} = route;

	const { length: routeLength } = latlngs;
	const offsetPoints = Math.round(Math.random() * routeLength);
	const totalRouteSeconds = routeLength * secondsPerPoint;

	const constructors = {
		deviceID,
		latlngs,
		routeLength,

		offsetPoints,
		secondsPerPoint,
		totalRouteSeconds
	};

	const newDevice = {
		...demoDevice,
		...constructors
	};

	generatedDevices[deviceID] = newDevice;

	return newDevice;
}

const demoDevice = {
	deviceID: null,
	latlngs: null,
	routeLength: null,

	offsetPoints: null,
	secondsPerPoint: null,
	totalRouteSeconds: null,

	getPointForTime(dateTime) {
		const totalPoints = Math.round(dateTime / 1000 / this.secondsPerPoint) + this.offsetPoints;
		return totalPoints % this.routeLength;
	},

	getLatLngForTime(dateTime) {
		const currentPoint = this.getPointForTime(dateTime);
		return this.latlngs[currentPoint];
	},

	getLatLngForTimeRange(fromDate, toDate) {
		const fromPoint = this.getPointForTime(fromDate);
		const toPoint = this.getPointForTime(toDate);
		return this.latlngs.slice(fromPoint, toPoint);
	},

	getLogRecordForTimeRange(fromDate, toDate) {

		const result = [];
		let currentTime = fromDate;
		let currentPoint = this.getPointForTime(fromDate);

		while (currentTime < toDate) {

			const latlng = this.latlngs[currentPoint];
			const speed = Math.round(Math.random() * 120);

			result.push(
				createLogRecord(currentTime, this.deviceID, latlng, speed)
			);

			currentTime += this.secondsPerPoint * 1000;

			currentPoint++;

			if (currentPoint >= this.routeLength) {
				currentPoint = 0;
			}
		}

		return result;
	},
};

function createLogRecord(dateTime, deviceID, latlng, speed) {

	const [latitude, longitude] = latlng;

	return {
		dateTime: new Date(dateTime).toISOString(),
		device: {
			id: deviceID
		},
		id: "demo",
		latitude,
		longitude,
		speed,
	};
}

